def lbm_converge(solver: SparsePscD3Q19, args: argparse.Namespace) -> tuple[int, float, bool, dict[str, float]]:
    previous = None
    residual = math.inf
    diag: dict[str, float] = {}
    for iteration in range(1, args.psc_max_steps + 1):
        diag = solver.collide_stream_psc()
        current = float(diag.get("mean_uz", 0.0))
        if previous is not None:
            residual = abs(current - previous) / max(abs(current), 1.0e-30)
        previous = current
        if iteration >= args.psc_min_steps and residual < args.residual_tol:
            return iteration, residual, True, diag
    return args.psc_max_steps, residual, False, diag


def lbm_transient_advance(
    solver: SparsePscD3Q19, args: argparse.Namespace
) -> tuple[int, float, bool, dict[str, float]]:
    """Advance an explicit number of physical LBM steps without steady iteration."""

    previous = float(np.mean(solver.uz))
    diag: dict[str, float] = {}
    for _ in range(args.lbm_steps_per_exchange):
        diag = solver.collide_stream_psc()
    current = float(diag.get("mean_uz", previous))
    residual = abs(current - previous) / max(abs(current), 1.0e-30)
    return args.lbm_steps_per_exchange, residual, True, diag


