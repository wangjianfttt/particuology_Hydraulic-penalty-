"""Dimensionally consistent PSC-IMB-DEM coupling driver.

This script advances the local pebble-bed window with either a quasi-steady
fluid-response exchange or a physically synchronized transient exchange:

1. read the current DEM particle state from LIGGGHTS library mode;
2. update the sparse D3Q19 partially saturated-cell IMB lattice;
3. iterate the LBM solver until the velocity residual reaches tolerance;
4. transfer node-summed hydrodynamic forces and torques to LIGGGHTS;
5. advance DEM by the integer number of substeps satisfying
   ``dt_LBM = n * dt_DEM``.

In ``quasi_steady`` mode each exchange solves the PSC-IMB field to a residual
tolerance before advancing DEM; the exchange count is not a transient physical
clock. In ``transient`` mode each exchange advances an explicit integer number
of LBM lattice steps and DEM advances by the integer number of stable substeps
covering exactly the same physical interval.

The driver is intentionally conservative: skeleton pebbles remain frozen by
the LIGGGHTS input file, fines receive hydrodynamic loads, and every macro-step
records the physical time, LBM convergence, force/torque moving averages, and
contact diagnostics needed for audit.
"""

