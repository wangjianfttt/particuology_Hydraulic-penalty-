def write_rows(path: Path, rows: list[dict[str, object]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=FIELDS)
        writer.writeheader()
        writer.writerows(rows)


def read_existing_rows(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open(newline="") as fh:
        reader = csv.DictReader(fh)
        if reader.fieldnames != FIELDS:
            raise ValueError(f"Cannot resume {path}: CSV fields do not match current schema")
        return list(reader)


def write_schema(path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="") as fh:
        writer = csv.writer(fh)
        writer.writerow(["field", "unit", "description"])
        for field in FIELDS:
            unit, description = FIELD_SCHEMA.get(field, ("", ""))
            writer.writerow([field, unit, description])


def contact_snapshot_for_fines(prefix: str, fine_ids: set[int]) -> tuple[float, float, float]:
    files = sorted(BASE.glob(f"{prefix}_contact_*.local"))
    if not files or not fine_ids:
        return 0.0, 0.0, 0.0
    frames = load_liggghts_local_frames(files[-1])
    if not frames:
        return 0.0, 0.0, 0.0
    contacts = parse_pair_gran_local(frames[-1])
    if not len(contacts.overlap):
        return 0.0, 0.0, 0.0
    fine_i = np.array([int(atom_id) in fine_ids for atom_id in contacts.ids[:, 0]], dtype=bool)
    fine_j = np.array([int(atom_id) in fine_ids for atom_id in contacts.ids[:, 1]], dtype=bool)
    fine_contact = fine_i | fine_j
    fine_fine = fine_i & fine_j
    max_force = float(np.max(contacts.force_magnitude[fine_contact])) if np.any(fine_contact) else 0.0
    return float(np.sum(fine_contact)), float(np.sum(fine_fine)), max_force


def retained_fraction_by_id(state_after, fine_ids: set[int]) -> float:
    """Fraction of the original fine IDs still inside the local box.

    LIGGGHTS may remove particles that leave a non-periodic local window.  The
    post-DEM state can therefore contain fewer atoms than the pre-DEM state, so
    retention must be computed by atom id rather than by reusing a stale boolean
    mask.
    """

    if not fine_ids:
        return 0.0
    visible = {int(atom_id): idx for idx, atom_id in enumerate(state_after.ids)}
    retained = 0
    for atom_id in fine_ids:
        idx = visible.get(atom_id)
        if idx is None:
            continue
        z = float(state_after.x[idx, 2])
        if 0.0 < z < BOX_SIZE:
            retained += 1
    return retained / float(len(fine_ids))


def checkpoint_dir_for(args: argparse.Namespace) -> Path:
    if args.checkpoint_dir is not None:
        return args.checkpoint_dir
    return args.output_csv.with_name(args.output_csv.stem + "_checkpoints")


def checkpoint_path(args: argparse.Namespace, macro_step: int) -> Path:
    return checkpoint_dir_for(args) / f"{args.dump_prefix}_macro_{macro_step:06d}.data"


def checkpoint_step_from_path(path: Path, dump_prefix: str) -> int | None:
    prefix = f"{dump_prefix}_macro_"
    if not path.name.startswith(prefix) or not path.name.endswith(".data"):
        return None
    raw = path.name[len(prefix) : -len(".data")]
    try:
        return int(raw)
    except ValueError:
        return None


def latest_available_checkpoint(args: argparse.Namespace, max_macro: int) -> tuple[int, Path] | None:
    checkpoints: list[tuple[int, Path]] = []
    checkpoint_dir = checkpoint_dir_for(args)
    if not checkpoint_dir.exists():
        return None
    for path in checkpoint_dir.glob(f"{args.dump_prefix}_macro_*.data"):
        step = checkpoint_step_from_path(path, args.dump_prefix)
        if step is not None and step <= max_macro:
            checkpoints.append((step, path))
    if not checkpoints:
        return None
    return max(checkpoints, key=lambda item: item[0])


def rel_for_liggghts(path: Path) -> str:
    try:
        return path.resolve().relative_to(ROOT).as_posix()
    except ValueError:
        return str(path.resolve())


def resume_state(args: argparse.Namespace, n_macro: int) -> tuple[list[dict[str, str]], int, str | None]:
    if not args.resume:
        return [], 0, None
    rows = read_existing_rows(args.output_csv)
    if not rows:
        return [], 0, None
    complete_rows = [row for row in rows if row.get("status") == "ok"]
    if len(complete_rows) != len(rows):
        raise ValueError(f"Cannot resume {args.output_csv}: non-ok rows are present")
    start_macro = len(rows)
    if start_macro >= n_macro:
        print(f"{args.output_csv} already has {start_macro}/{n_macro} complete rows")
        return rows, start_macro, None
    last_checkpoint = checkpoint_path(args, start_macro)
    if not last_checkpoint.exists():
        fallback = latest_available_checkpoint(args, start_macro)
        if fallback is None:
            raise FileNotFoundError(
                f"Cannot resume after macro {start_macro}: missing checkpoint {last_checkpoint}. "
                "Re-run without --resume or start from an older backed-up checkpoint."
            )
        fallback_macro, fallback_checkpoint = fallback
        if fallback_macro <= 0:
            raise FileNotFoundError(
                f"Cannot resume after macro {start_macro}: no positive checkpoint found in "
                f"{checkpoint_dir_for(args)}"
            )
        print(
            f"Cannot resume after macro {start_macro}: missing checkpoint {last_checkpoint}. "
            f"Falling back to macro {fallback_macro} checkpoint {fallback_checkpoint}."
        )
        rows = rows[:fallback_macro]
        write_rows(args.output_csv, rows)
        start_macro = fallback_macro
        last_checkpoint = fallback_checkpoint
    return rows, start_macro, rel_for_liggghts(last_checkpoint)


def main() -> None:
    args = parse_args()
    if args.total_time <= 0.0 or args.dt_lbm <= 0.0:
        raise ValueError("--T-total and --dt-LBM must be positive")
    if args.lbm_steps_per_exchange <= 0:
        raise ValueError("--lbm-steps-per-exchange must be positive")
    if not args.template.exists():
        raise FileNotFoundError(args.template)
    data_path = ROOT / args.data_file if not Path(args.data_file).is_absolute() else Path(args.data_file)
    wall_start = time.time()

    scale_args = SimpleNamespace(**vars(args))
    scale_info = apply_si_scaling(scale_args)
    for key in ("psc_force_scale", "psc_torque_scale", "velocity_lu_scale", "angular_lu_scale"):
        setattr(args, key, getattr(scale_args, key))
    if not args.use_si_scaling and not args.allow_unscaled_regression:
        raise ValueError(
            "Production PSC-IMB-DEM runs require --use-si-scaling. "
            "Use --allow-unscaled-regression only for explicitly labelled numerical regression cases."
        )
    if args.coupling_mode == "transient":
        if not args.use_si_scaling:
            raise ValueError("Transient coupling requires --use-si-scaling")
        if args.body_acceleration_z is None:
            raise ValueError("Transient coupling requires --body-acceleration-z in physical m/s^2")
        lattice_dt = float(scale_info["dt_s"])
        args.dt_lbm = lattice_dt * args.lbm_steps_per_exchange
        args.force_z = args.body_acceleration_z * lattice_dt * lattice_dt / float(scale_info["dx_m"])
        ratio_total = args.total_time / args.dt_lbm
        if abs(ratio_total - round(ratio_total)) > 1.0e-10:
            raise ValueError(
                "Transient --T-total must be an integer multiple of the physical LBM exchange interval "
                f"{args.dt_lbm:.16e} s; got ratio={ratio_total:.16g}"
            )

    dt_dem, n_dem = dem_substeps(args)
    n_macro = int(math.ceil(args.total_time / args.dt_lbm))

    if args.dry_run:
        rows = empty_schedule_rows(args, dt_dem, n_dem)
        write_rows(args.output_csv, rows)
        write_schema(args.schema_csv or args.output_csv.with_name(args.output_csv.stem + "_schema.csv"))
        print(f"dt_LBM/dt_DEM={n_dem}; wrote {args.output_csv}")
        return

    if not data_path.exists():
        raise FileNotFoundError(data_path)

    rows, start_macro, resume_data_file = resume_state(args, n_macro)
    if start_macro >= n_macro:
        return
    input_data_file = resume_data_file or args.data_file
    if start_macro == 0:
        for pattern in (f"{args.dump_prefix}_traj_*.dump", f"{args.dump_prefix}_contact_*.local"):
            for old in BASE.glob(pattern):
                old.unlink()
    case_input = write_synced_input(args.template, input_data_file, args.dump_prefix, dt_dem, args.youngs_modulus)

    if rows:
        force_ma = float(rows[-1].get("force_norm_sliding_average_n", 0.0) or 0.0)
        torque_ma = float(rows[-1].get("torque_norm_sliding_average_nm", 0.0) or 0.0)
    else:
        force_ma = 0.0
        torque_ma = 0.0
    alpha_ma = 0.1
    early_stop_triggered = False
    early_stop_reason = ""
    solver: SparsePscD3Q19 | None = None

    with LiggghtsForceCoupler(case_input, quiet=True) as dem:
        for macro in range(start_macro, n_macro):
            state = dem.state()
            particle_table = particles_from_state(state)
            fine_mask = particle_table[:, 1] == 2
            fine_ids = {int(atom_id) for atom_id in particle_table[fine_mask, 0]}
            particles = solver_particles(state, args)
            if solver is None:
                solver = SparsePscD3Q19(
                    n=args.resolution,
                    particles=particles,
                    tau=args.tau,
                    subsamples=args.psc_subsamples,
                    body_force_z=args.force_z,
                )
            else:
                solver.particles = particles
                if solver.hydro_forces.shape[0] != len(particles):
                    solver.hydro_forces = np.zeros((len(particles), 3), dtype=float)
                    solver.hydro_torques = np.zeros((len(particles), 3), dtype=float)

            if args.coupling_mode == "transient":
                lbm_iters, lbm_residual, converged, diag = lbm_transient_advance(solver, args)
            else:
                lbm_iters, lbm_residual, converged, diag = lbm_converge(solver, args)
            forces = args.psc_force_scale * solver.hydro_forces
            torques = args.psc_torque_scale * solver.hydro_torques
            forces[~fine_mask] = 0.0
            torques[~fine_mask] = 0.0
            dem.set_dragforces(state.ids, forces)
            if args.apply_psc_torque:
                dem.set_hdtorques(state.ids, torques)
            dem.run(n_dem)
            state_after = dem.state()

            f_norm = np.linalg.norm(forces[fine_mask], axis=1) if np.any(fine_mask) else np.zeros(0)
            t_norm = np.linalg.norm(torques[fine_mask], axis=1) if np.any(fine_mask) else np.zeros(0)
            force_mean = float(np.mean(f_norm)) if len(f_norm) else 0.0
            torque_mean = float(np.mean(t_norm)) if len(t_norm) else 0.0
            force_ma = (1.0 - alpha_ma) * force_ma + alpha_ma * force_mean
            torque_ma = (1.0 - alpha_ma) * torque_ma + alpha_ma * torque_mean
            retained = retained_fraction_by_id(state_after, fine_ids)
            n_fine_contacts, n_fine_fine, max_fine_force = contact_snapshot_for_fines(args.dump_prefix, fine_ids)
            porosity = 1.0 - float(diag.get("solid_volume_lu", 0.0)) / float(args.resolution**3)
            row = {
                "case_id": args.case_id,
                "seed": args.seed,
                "data_file": args.data_file,
                "dump_prefix": args.dump_prefix,
                "coupling_mode": args.coupling_mode,
                "use_si_scaling": int(args.use_si_scaling),
                "macro_step": macro + 1,
                "physical_time_s": min((macro + 1) * args.dt_lbm, args.total_time),
                "dt_lbm_s": args.dt_lbm,
                "dt_dem_s": dt_dem,
                "dem_substeps": n_dem,
                "lbm_iterations": lbm_iters,
                "lbm_residual": lbm_residual,
                "lbm_converged": int(converged),
                "imb_momentum_residual": float(diag.get("momentum_residual", math.nan)),
                "porosity_psc": porosity,
                "mean_uz_lu": float(diag.get("mean_uz", math.nan)),
                "superficial_uz_lu": float(np.mean(solver.uz * (1.0 - np.clip(solver.solid, 0.0, 1.0)))),
                "n_particles": len(particles),
                "n_fines": int(np.sum(fine_mask)),
                "retained_fraction": retained,
                "force_norm_mean_n": force_mean,
                "force_norm_max_n": float(np.max(f_norm)) if len(f_norm) else 0.0,
                "torque_norm_mean_nm": torque_mean,
                "torque_norm_max_nm": float(np.max(t_norm)) if len(t_norm) else 0.0,
                "force_norm_sliding_average_n": force_ma,
                "torque_norm_sliding_average_nm": torque_ma,
                "n_fine_contacts": n_fine_contacts,
                "n_fine_fine_contacts": n_fine_fine,
                "max_fine_contact_force_n": max_fine_force,
                "psc_force_scale_n_per_lu": args.psc_force_scale,
                "psc_torque_scale_nm_per_lu": args.psc_torque_scale,
                "velocity_lu_per_m_s": args.velocity_lu_scale,
                "angular_lu_per_rad_s": args.angular_lu_scale,
                "status": "ok" if converged else "lbm_not_converged",
            }
            rows.append(row)
            if args.checkpoint_every > 0 and (macro + 1) % args.checkpoint_every == 0:
                dem.write_data(checkpoint_path(args, macro + 1))
            write_rows(args.output_csv, rows)
            write_schema(args.schema_csv or args.output_csv.with_name(args.output_csv.stem + "_schema.csv"))
            print(
                f"macro={macro + 1:04d}/{n_macro} t={row['physical_time_s']:.3e}s "
                f"sub={n_dem} lbm_it={lbm_iters} res={lbm_residual:.2e} "
                f"retained={retained:.3f} fine_contacts={n_fine_contacts:.0f}"
            )
            if (
                args.stop_when_retained_below >= 0.0
                and (macro + 1) >= args.min_macro_before_stop
                and retained <= args.stop_when_retained_below
            ):
                early_stop_triggered = True
                early_stop_reason = (
                    f"retained_fraction {retained:.6g} <= "
                    f"{args.stop_when_retained_below:.6g}"
                )
                print(
                    "early stop: all relevant fines have left the local statistics "
                    f"window ({early_stop_reason})"
                )
                break

    scale_csv = args.output_csv.with_name(args.output_csv.stem + "_scales.csv")
    with scale_csv.open("w", newline="") as fh:
        writer = csv.writer(fh)
        writer.writerow(["quantity", "value"])
        for key, value in scale_info.items():
            writer.writerow([key, value])
        writer.writerow(["case_input", rel(case_input)])
        writer.writerow(["youngs_modulus_pa", args.youngs_modulus])
        writer.writerow(["case_id", args.case_id])
        writer.writerow(["seed", args.seed])
        writer.writerow(["data_file", args.data_file])
    metadata_path = args.metadata_json or args.output_csv.with_name(args.output_csv.stem + "_metadata.json")
    metadata = {
        "case_id": args.case_id,
        "seed": args.seed,
        "data_file": args.data_file,
        "dump_prefix": args.dump_prefix,
        "template": rel(args.template),
        "generated_liggghts_input": rel(case_input),
        "output_csv": rel(args.output_csv),
        "schema_csv": rel(args.schema_csv or args.output_csv.with_name(args.output_csv.stem + "_schema.csv")),
        "scale_csv": rel(scale_csv),
        "total_time_s": args.total_time,
        "dt_lbm_s": args.dt_lbm,
        "dt_dem_s": dt_dem,
        "dem_substeps": n_dem,
        "macro_steps_requested": n_macro,
        "macro_steps_completed": len(rows),
        "early_stop_triggered": early_stop_triggered,
        "early_stop_reason": early_stop_reason,
        "stop_when_retained_below": args.stop_when_retained_below,
        "min_macro_before_stop": args.min_macro_before_stop,
        "resolution": args.resolution,
        "tau": args.tau,
        "force_z": args.force_z,
        "residual_tol": args.residual_tol,
        "psc_min_steps": args.psc_min_steps,
        "psc_max_steps": args.psc_max_steps,
        "youngs_modulus_pa": args.youngs_modulus,
        "apply_psc_torque": args.apply_psc_torque,
        "use_si_scaling": args.use_si_scaling,
        "coupling_regime": args.coupling_mode,
        "physical_time_basis": (
            "explicit_lbm_and_dem_transient_time"
            if args.coupling_mode == "transient"
            else "dem_exchange_time_with_quasi_steady_fluid_response"
        ),
        "dem_exchange_time_s": args.dt_lbm,
        "lbm_lattice_time_scale_s": scale_info["dt_s"],
        "lbm_steps_per_exchange": args.lbm_steps_per_exchange,
        "body_acceleration_z_m_s2": args.body_acceleration_z,
        "unscaled_regression_only": bool(args.allow_unscaled_regression and not args.use_si_scaling),
        "wall_time_s": time.time() - wall_start,
        "scale_info": scale_info,
    }
    metadata_path.parent.mkdir(parents=True, exist_ok=True)
    metadata_path.write_text(json.dumps(metadata, indent=2, sort_keys=True) + "\n")
    print(f"wrote {args.output_csv}")
    print(f"wrote {scale_csv}")
    print(f"wrote {metadata_path}")


if __name__ == "__main__":
    main()
