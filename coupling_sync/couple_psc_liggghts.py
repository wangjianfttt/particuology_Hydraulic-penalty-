def dem_substeps(args: argparse.Namespace) -> tuple[float, int]:
    if args.coupling_mode == "transient" and args.dt_dem is None:
        if args.dt_dem_max <= 0.0:
            raise ValueError("--dt-dem-max must be positive")
        n = int(math.ceil(args.dt_lbm / args.dt_dem_max))
        return args.dt_lbm / n, n
    if args.dt_dem is None:
        if args.auto_subcycles <= 0:
            raise ValueError("--auto-subcycles must be positive when --dt-DEM is omitted")
        return args.dt_lbm / args.auto_subcycles, args.auto_subcycles
    ratio = args.dt_lbm / args.dt_dem
    n = int(round(ratio))
    if n <= 0 or abs(ratio - n) > 1.0e-10:
        raise ValueError(f"dt_LBM must equal n*dt_DEM; got ratio={ratio:.16g}")
    return args.dt_dem, n


def write_synced_input(template: Path, data_file: str, dump_prefix: str, dt_dem: float, youngs_modulus: float) -> Path:
    BASE.mkdir(parents=True, exist_ok=True)
    dump_base = BASE.relative_to(ROOT)
    lines: list[str] = []
    inserted_hard_particles = False
    for raw in template.read_text().splitlines():
        line = raw.strip()
        if line.startswith("read_data "):
            lines.append(f"read_data {data_file}")
        elif line.startswith("fix m1 all property/global youngsModulus"):
            if youngs_modulus > 1.0e9 and not inserted_hard_particles:
                lines.append("hard_particles yes")
                inserted_hard_particles = True
            lines.append(
                "fix m1 all property/global youngsModulus peratomtype "
                f"{youngs_modulus:.8e} {youngs_modulus:.8e}"
            )
        elif line.startswith("timestep "):
            lines.append(f"timestep {dt_dem:.16e}")
        elif line.startswith("dump traj all custom"):
            lines.append(
                "dump traj all custom 100 "
                f"{dump_base / (dump_prefix + '_traj_*.dump')} "
                "id type x y z vx vy vz fx fy fz radius c_contacts"
            )
        elif line.startswith("dump dlocal all local"):
            lines.append(
                "dump dlocal all local 100 "
                f"{dump_base / (dump_prefix + '_contact_*.local')} "
                "c_cpgl[1] c_cpgl[2] c_cpgl[3] c_cpgl[4] c_cpgl[5] c_cpgl[6] "
                "c_cpgl[7] c_cpgl[8] c_cpgl[9] c_cpgl[10] c_cpgl[11] c_cpgl[12] "
                "c_cpgl[13] c_cpgl[14] c_cpgl[15] c_cpgl[16] c_cpgl[17] c_cpgl[18] c_cpgl[19]"
            )
        else:
            lines.append(raw)
    path = BASE / f"{dump_prefix}_synced.in"
    path.write_text("\n".join(lines) + "\n")
    return path


